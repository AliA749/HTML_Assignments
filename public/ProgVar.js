function Average() {
  var inputValue1 = parseFloat(document.getElementById("CourseGRADE").value);
  var inputValue2 = parseFloat(document.getElementById("CourseGRADE2").value);
  var inputValue3 = parseFloat(document.getElementById("CourseGRADE3").value);
  var inputValue4 = parseFloat(document.getElementById("CourseGRADE4").value);
  var inputValue5 = parseFloat(document.getElementById("CourseGRADE5").value);
  var inputValue6 = parseFloat(document.getElementById("CourseGRADE6").value);
  var inputValue7 = parseFloat(document.getElementById("CourseGRADE7").value);
  var inputValue8 = parseFloat(document.getElementById("CourseGRADE8").value);

  var total =
    inputValue1 +
    inputValue2 +
    inputValue3 +
    inputValue4 +
    inputValue5 +
    inputValue6 +
    inputValue7 +
    inputValue8;
  var average = total / 8;

  var gpa;
  gpa=average*4/100;
  
  var Lettergrade;
switch (true) {
  case average >= 100:
    Lettergrade = "A+";
    break;
  case average >= 90:
    Lettergrade = "A";
    break;
  case average >= 80:
    Lettergrade = "B";
    break;
  case average >= 70:
    Lettergrade = "C";
    break;
    case average >= 60:
    Lettergrade = "D";
    break;
    case average >= 50:
    Lettergrade = "E";
    break;
  default:
    Lettergrade = "F";
    break;
}

  document.getElementById("LGrade").innerText =
    "The GPA of the grades is: " + Lettergrade + ".";

  document.getElementById("Gpa").innerText =
    "The GPA of the grades is: " + gpa + ".";

  document.getElementById("Average").innerText =
    "The average of the grades is: " + average + ".";
    
}
